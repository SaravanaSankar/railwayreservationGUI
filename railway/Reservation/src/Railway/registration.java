    public boolean verifyfields(){
        String fname = textfullname.getText();
        String uname = textusername.getText();
        String pass = passpassword.getText();
        String email = textemail.getText();
        if(fname.trim().equals("") || uname.trim().equals("")||pass.trim().equals("")||email.trim().equals("")){
            JOptionPane.showMessageDialog(null,"One or More fileds are missing");
            return false;
        }
        else{
            return true;
        }
    }
    public boolean checkusername(String uname){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        String query = "SELECT * FROM `userdetails` WHERE username = ?";
        try {
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, uname);
            rs = st.executeQuery();
             if(rs.next())
        {
            username_exist = true;
            JOptionPane.showMessageDialog(null, "This user is Already Taken,take Another one", "username failed", 2);
        }
        } catch (SQLException ex) {
            Logger.getLogger(registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exist;
       
    }
