    private void registerbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerbtnActionPerformed
        // TODO add your handling code here:
        String fname = textfullname.getText();
        String uname = textusername.getText();
        String pass = passpassword.getText();
        String email = textemail.getText();
        if(verifyfields()){
            if(!checkusername(uname))
            {
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery ="INSERT INTO `userdetails`(`full_name`, `username`, `password`, `email`) VALUES (?,?,?,?)";
                try {
                    ps = My_CNX.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, fname);
                    ps.setString(2, uname);
                    ps.setString(3, pass);
                    ps.setString(4, email);
                    if(ps.executeUpdate()!=0){
                        JOptionPane.showMessageDialog(null,"Your Account has been added");
                    }else{
                        JOptionPane.showMessageDialog(null,"Error check your information");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(registration.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_registerbtnActionPerformed

